/****************************************************************************************************
* cc_service_cfg_recv()
****************************************************************************************************/
cc_result cc_service_cfg_recv(cc_int8 *dstname,cc_int8 *srcname,cc_uint8 *data,cc_uint16 len,cc_service_msgtype_enum type)
{
    cc_result result = CC_E_OK;

    do
    {
        /* check parameter */
        CHECK_PTR(result,dstname);
        CHECK_PTR(result,srcname);
        CHECK_PTR(result,data);
        /* call service recv */
        cc_service_recv(dstname,srcname,data,len,type);
    }while(0u);
    
    return result;
}


